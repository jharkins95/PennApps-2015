package com.example.jthem.PennApps2015;

import edu.princeton.cs.introcs.StdDraw;

public class Controls {
	public static void readKey() {
		char key = StdDraw.nextKeyTyped();
		switch (key) {
		
		case 'a': // left
			break;
		case 'd': // right
			break;
		case 'w': // up
			break;
		case 's': // down
			break;
		}
	}
}
